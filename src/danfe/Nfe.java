package danfe;

public class Nfe
{
	private int nrNfiscal;
	private String serie;
	private String chaveAcesso;
	private String xml;
	/**
	 * @return the nrNfiscal
	 */
	public int getNrNfiscal()
	{
		return nrNfiscal;
	}
	/**
	 * @param nrNfiscal the nrNfiscal to set
	 */
	public void setNrNfiscal(int nrNfiscal)
	{
		this.nrNfiscal = nrNfiscal;
	}
	/**
	 * @return the serie
	 */
	public String getSerie()
	{
		return serie;
	}
	/**
	 * @param serie the serie to set
	 */
	public void setSerie(String serie)
	{
		this.serie = serie;
	}
	/**
	 * @return the chaveAcesso
	 */
	public String getChaveAcesso()
	{
		return chaveAcesso;
	}
	/**
	 * @param chaveAcesso the chaveAcesso to set
	 */
	public void setChaveAcesso(String chaveAcesso)
	{
		this.chaveAcesso = chaveAcesso;
	}
	/**
	 * @return the xml
	 */
	public String getXml()
	{
		return xml;
	}
	/**
	 * @param xml the xml to set
	 */
	public void setXml(String xml)
	{
		this.xml = xml;
	}
	
	
}
